#int - e sigla para que seja inseridos dados n° inteiro
#float - para numeros reais (ex.:7.5, 8.5) numeros com casas decimais


nota_1 = int (input("digite o valor da nota 1: "))
nota_2 = int (input("digite o valor da nota 2: "))
